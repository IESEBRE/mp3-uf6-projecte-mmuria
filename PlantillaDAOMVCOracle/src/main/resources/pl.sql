CREATE TABLE MOTOS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    MODEL VARCHAR2(100) NOT NULL,
    PES NUMBER(10) NOT NULL,
    ES_EN_MARCHES NUMBER(1)
);

CREATE OR REPLACE TRIGGER trg_motos_id
BEFORE INSERT ON MOTOS
FOR EACH ROW
BEGIN
    SELECT NVL(MAX(ID), 0) + 1 INTO :NEW.ID FROM MOTOS;
END;
